    private void guardarPacienteEnArchivo(Paciente  emp) {
        try (FileWriter fw = new FileWriter("Pacientes.txt", true); BufferedWriter bw = new BufferedWriter(fw); PrintWriter out = new PrintWriter(bw)) {

            out.println("Pacientes;" + emp.getNombre() + ";" + emp.getId() + ";" + emp.getEdad() + ";"
                    + emp.getCorreo()+ ";" + emp.getTelefono()+ ";" + emp.getEstado());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el archivo ");
        }
    }
