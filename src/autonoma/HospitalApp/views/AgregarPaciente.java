    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtNombre.getText().trim();
        String id = TXTId.getText().trim();
        String edad = txtEdad.getText().trim();
        String correo = txtCorreo.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String estado = txtEstado.getText().trim();
   /**
    * validamos que los campos tengan informacion
    */
    if (nombre.isEmpty() || id.isEmpty() || edad.isEmpty() || correo.isEmpty() ||
        telefono.isEmpty() || estado.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
        return;
    }

    try {
        /**
         * los creamos a valores numericos
         */

        int edadNum = Integer.parseInt(txtEdad.getText().trim());
        int telefonoNum = Integer.parseInt(txtTelefono.getText().trim());

        Paciente paciente = new Paciente(nombre, Integer.parseInt(id), edadNum, correo, telefonoNum, estado);
        hospital.agregarPaciente(paciente); 
        guardarPacienteEnArchivo(paciente); 

        JOptionPane.showMessageDialog(this, "Paciente agregado exitosamente");
        this.dispose();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor ingresa correctamente los campos numéricos.");
    }
}{

    }//GEN-LAST:event_btnAgregarActionPerformed

